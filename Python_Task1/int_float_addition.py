#program for Addition of two integers:
String1 = "500"
String2 = "250"


#Converting string to integer:
Integer1 = int(String1)
Integer2 = int(String1)


#addition of two integers
Addition = Integer1 + Integer2


#printing Addition:
print("\nAddition of Integer1 and Integer2 is : ",Addition)


#converting string to float:
Float_no1 = float(String1)
Float_no2 = float(String2)


#multiplication of two float numbers:
Multiplication = Float_no1 * Float_no2

#printing multiplication
print("\nMultiplication of Float_no1 and Float_no2 is : ",Multiplication)

